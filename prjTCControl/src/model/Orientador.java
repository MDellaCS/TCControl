package model;

public class Orientador extends Pessoa {

	private String disciplinas;
	private String alunosOrientados;

	public String getDisciplinas() {
		return disciplinas;
	}

	public void setDisciplinas(String disciplinas) {
		this.disciplinas = disciplinas;
	}

	public String getAlunosOrientados() {
		return alunosOrientados;
	}

	public void setAlunosOrientados(String alunosOrientados) {
		this.alunosOrientados = alunosOrientados;
	}

}
